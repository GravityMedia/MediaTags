namespace GravityMedia\Metadata\ID3v2;

/**
 * ID3v2 frame class.
 *
 * @package GravityMedia\Metadata
 */
class Frame
{
    /**
     * @var string
     */
    protected $name;

    /**
     * Get name.
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set name.
     *
     * @param string $name
     *
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }
}
